# MAGIC |Q1|安全在庫を下回りますが、プロ会員のお客様が工事日程に合わせて取り置きを希望しています。受付できますか？|【原則】安全在庫を下回る場合は受付不可と明示する。<br>【例外】プロ会員・法人案件は店長判断で例外可と整理する。<br>【判断レベル】要店長判断と明示する。<br>【接客トーク例】顧客体験を損なわない言い回しを提示する。|原則だけで「不可」と断定してはいけない。例外条件と判断権限（店長判断）を明示する必要がある。|
# MAGIC |Q2|開封済みの塗料を返品したいと言われましたが、色味がイメージと違うとのことです。規定上は不可ですがどう対応しますか？|【原則】開封済み化学製品は返品不可と明示する。<br>【例外】初期不良（変色・異臭・固化等）の場合は例外であることを明示する。<br>【優先対応】代替色の提案を優先する方針を説明する。|「不可です」だけでは不十分。代替提案と初期不良の例外条件を整理すべき。|
# MAGIC |Q3|DIY未経験のお客様がインパクトドライバーを買いに来ましたが、プロ向けの高額モデルを手に取っています。どう対応しますか？|【対応】まず用途を確認する。<br>【提案】初心者には入門モデル（10.8V等）を提案し、慣れてからステップアップを案内する。<br>【注意】いきなり高額品を否定しない。用途に合えばそのまま案内する。|「それは高いですよ」「素人には難しいと思います」はNG。用途確認→適切な提案の流れを示すべき。|
# MAGIC |Q4|強い口調で「買った工具が壊れた、今すぐ返金しろ」と要求されています。保証期間内ですがレシートがありません。どう対応しますか？|【対応】まず傾聴と共感を優先する。<br>【確認】会員登録・決済履歴で購入事実を確認する。<br>【判断レベル】確認取れない場合は店長判断。高額品は本部判断と明示する。<br>【接客トーク例】感情を刺激しない言い回しを提示する。|規定だけで突っぱねるのはNG。傾聴→事実確認→本部判断の流れを示すべき。|

