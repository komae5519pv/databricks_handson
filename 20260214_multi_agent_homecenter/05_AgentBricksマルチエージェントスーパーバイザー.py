# MAGIC ## 2. マルチエージェントスーパーバイザーを使ってみる
# MAGIC
# MAGIC |質問番号|質問内容|主な参照元（エージェント）|狙い・意図|
# MAGIC |----|----------------------------------------------|------------------------------|----------------------------------------------|
# MAGIC |Q1|受付済みの取り置き予約を確認して|UC関数 + Genie|UC関数でログインユーザーを特定→Genieで自分の担当予約だけ自動フィルタ。Genie単体では複数ラウンド必要な操作がスーパーバイザーなら一発で完結する。|
# MAGIC |Q2|佐藤工務店様の予約、確保できていますか？|Genie + RAG|Q1の結果を受けて、Genieで販売可能在庫を確認し、RAGで安全在庫基準・プロ会員例外条件を参照。数値＋ルール統合判断。|
# MAGIC |Q3|このお客様、ウッドデッキも作りたいらしい。多摩店で資材揃う？|RAG → Genie|RAGがDIY相談フローから必要資材を判断→Genieが多摩店の在庫を一括チェック→不足分の代替案まで出せるか。★最大の見せ場。|
