# MAGIC %md
# MAGIC ## 3. デモでの見せ方
# MAGIC
# MAGIC ### ストーリーライン
# MAGIC
# MAGIC ```
# MAGIC 【前半】Genieだけで分析（構造化データの世界）
# MAGIC   「媒体別KPI見てみましょう」→「返品率も加えると…LINEが高い」
# MAGIC   → 数字はわかった。でも"なぜ"がわからない。
# MAGIC
# MAGIC 【中盤】ナレッジアシスタントで通話記録を検索（非構造化データの世界）
# MAGIC   「実際に顧客がどんなことを言っているか聞いてみましょう」
# MAGIC   → 生の声が見える。でも全体像がわからない。
# MAGIC
# MAGIC 【後半】マルチエージェントで統合分析（★最大の見せ場）
# MAGIC   「一つの質問で、数字と声の両方を出してみましょう」
# MAGIC   → Q1を投げる。Genieが数字を、RAGが声を、スーパーバイザーが統合回答を返す。
# MAGIC   → 「これが、構造化データと非構造化データを一つのプラットフォームで扱える価値です」
# MAGIC ```
# MAGIC
# MAGIC ### キーメッセージ
# MAGIC
# MAGIC > **Genieだけ** → 「LINEの返品率が高い」（What）<br>
# MAGIC > **RAGだけ** → 「割引が違うと怒っている声がある」（Voice）<br>
# MAGIC > **マルチエージェント** → 「LINEの返品率18%の原因は割引条件の表記不統一。顧客は"広告と違う"と感じている。対策は媒体間の条件統一」（What + Why + How）

