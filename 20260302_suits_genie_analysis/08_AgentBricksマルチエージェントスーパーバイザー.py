# MAGIC ## 2. マルチエージェントスーパーバイザーを使ってみる
# MAGIC
# MAGIC ### デモシナリオ: 「数字」×「声」で経営判断が変わる
# MAGIC
# MAGIC |質問番号|質問内容|主な参照先|狙い・意図|
# MAGIC |----|----------------------------------------------|------------------------------|----------------------------------------------|
# MAGIC |Q1|LINEチャネルの返品率が高い原因を分析してください。数字と実際の顧客の声の両方から教えて。|Genie + RAG|★最大の見せ場。Genieが「LINE返品率18%、割引条件違いが55%」と数字を出し、RAGが「LINEで30%OFFって書いてあったのに20%しか…」と顧客の生の声を出す。数字だけでは見えない原因が、声と合わせて初めて腹落ちすることを体験させる。|
# MAGIC |Q2|割引条件にトラブルがある顧客は、トラブルがない顧客と比べてどのくらい返品しやすいですか？実際にどんな苦情が出ていますか？|Genie + RAG|Genieが返品率の差（2-3倍）を定量化し、RAGが「広告にそんな条件書いてなかった」「話が違う」といった具体的な苦情を提示。統計と実態の両面で問題の深刻さを示す。|
# MAGIC |Q3|配送や納期に関する問い合わせの全体像を教えてください。件数と、どんな問題が起きているかの両方で。|Genie + RAG|Genieが件数・媒体別分布を出し、RAGが「面接が明日なのに届かない」「追跡が動かない」等の具体事例を提示。急ぎ対応の体制整備への示唆。|
# MAGIC |Q4|コールセンターの対応品質が売上にどう影響しているか教えてください。問題が解決した顧客としていない顧客で購買行動に差はありますか？|Genie + RAG|Genieが解決/未解決顧客の購買金額差を出し、RAGが「丁寧に対応してもらえたのでまた買います」「全然解決しなかった、もう買わない」等の声を出す。コールセンター投資の正当化。|

