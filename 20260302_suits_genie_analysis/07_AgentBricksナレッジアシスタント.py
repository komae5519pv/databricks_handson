# MAGIC ### ラベル付きセッション
# MAGIC
# MAGIC |質問番号|質問|ガイドライン|
# MAGIC | ---- | --------------------------- | --------------------------------------------- |
# MAGIC |Q1|LINEの広告で割引率が違うと怒っている顧客の通話を教えて|【検索結果】割引条件クレームの通話を正しく検索できること。【引用】transcript から顧客の具体的な発言を引用すること。「20%オフだと思ったのに…」等の声が含まれること。【パターン】LINEチャネル経由に集中していること、条件の小さい注記が原因であることを指摘できると良い。|
# MAGIC |Q2|裾上げやお直しに関する問い合わせの事例を見せてください|【検索結果】お直しシナリオの通話を正しく検索できること。【引用】「裾上げの仕上がりが短い」「お直し期間はどのくらい？」等の具体的な声を引用。【パターン】EC購入後のお直し対応、店舗での裾上げ仕上がりへの不満、お直し期間の問い合わせ等のパターンを整理すること。|
# MAGIC |Q3|返品したいけど迷っている顧客の通話を探してください|【検索結果】返品・交換シナリオの通話を検索。【引用】顧客の心理（迷い、不安）が見える発言を引用。【パターン】返品理由の傾向（サイズ不一致 vs イメージ違い vs 不良品）を整理。タグ付き/なしの対応の違いも示せると良い。|

