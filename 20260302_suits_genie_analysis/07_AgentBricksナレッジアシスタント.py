# MAGIC ## 1. 通話ログ ナレッジアシスタントを作る
# MAGIC
# MAGIC Agent Bricks -> ナレッジアシスタント -> ビルド<br>
# MAGIC 名前: `cc-call-log-knowledge`<br>
# MAGIC 説明: `コールセンター通話記録（5,000件）を検索し、顧客の生の声・オペレーター対応事例・問い合わせパターンを提示するナレッジアシスタント`<br>
# MAGIC ナレッジソースを設定<br>
# MAGIC
# MAGIC |No|タイプ|ソース|名前|コンテンツを説明|
# MAGIC | --- | --- | --- | --- | --- |
# MAGIC |1|`UCファイル`|`/Volumes/komae_demo_v4/call_center/raw/call_logs/`|`call_logs`|`コールセンター通話記録（JSONL形式、5,000件）。各レコードにcall_id、顧客情報（氏名・年齢・性別・都道府県）、問い合わせ種別（苦情/相談/要望/確認/質問）、問い合わせ詳細、通話記録テキスト（transcript：オペレーターと顧客の会話全文）、オペレーター対応内容、提案された解決策、解決状況、顧客満足度を含む。主なシナリオ：割引条件クレーム、返品・交換、配送・納期、注文変更・キャンセル、在庫確認、商品不良・破損、他社比較、お直し（裾上げ）、請求・支払い、お手入れ・メンテナンス。`|
# MAGIC
# MAGIC オプション -> 手順設定（オプション）:<br>
# MAGIC ```
# MAGIC * あなたはコールセンター通話記録の検索・分析アシスタントです
# MAGIC * 通話記録（transcript）から、質問に関連する顧客の声・オペレーター対応事例を検索して提示してください
# MAGIC * 数値分析・集計は行わないでください（数値はGenieに任せます）
# MAGIC * 通話記録に含まれない情報については「通話記録からは確認できません」と回答してください
# MAGIC * 必ず日本語で回答してください
# MAGIC * 検索結果を提示する際は、以下の構造で整理してください
# MAGIC
# MAGIC 【検索結果サマリ】
# MAGIC  該当する通話の概要（件数・傾向）
# MAGIC
# MAGIC 【代表的な顧客の声】（2〜3件）
# MAGIC  - 通話ID / 問い合わせ種別
# MAGIC  - 顧客の発言（transcript からの引用）
# MAGIC  - オペレーターの対応
# MAGIC
# MAGIC 【共通するパターン・気づき】
# MAGIC  複数の通話に共通する傾向があれば指摘する
# MAGIC ```
# MAGIC
